#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t i;
	size_t temp;

	i = 0;
	if (!dest || !src)
		return (0);
	temp = ft_strlen(src);
	if (size == 0)
		return (temp);
	while (i < temp && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	if (size > 0)
		dest[i] = '\0';
	return (temp);
}
